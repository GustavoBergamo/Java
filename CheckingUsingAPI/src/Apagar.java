public class Apagar {
    private long id;
    private String name;
    
    public Apagar() {
        
    }

    public Apagar(long id, String name) {
        this.id = id;
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }
    
    
}
